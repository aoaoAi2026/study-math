def save(topicId, topicName, category, qs):
    for q in qs:
        q.setdefault('image', '')
        q.setdefault('options', [])
        q.setdefault('acceptableAnswers', [])
        q.setdefault('solution', [])
        q.setdefault('hints', [])
        q.setdefault('commonMistakes', [])
        q.setdefault('source', {'type': '原创', 'name': '内部题库', 'year': 2026})
        q.setdefault('knowledgePoints', [])
        q.setdefault('estimatedTime', 90)
        q.setdefault('irtParams', {'a': 1.0, 'b': 0.0, 'c': 0.25})
        q.setdefault('tags', [])
    data = {
        'topicId': topicId, 'topicName': topicName,
        'grade': 1, 'category': category, 'version': '1.0',
        'totalQuestions': len(qs), 'questions': qs
    }
    fp = os.path.join(OUT, topicId + '.json')
    with open(fp, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f'OK: {topicId} ({topicName}, {len(qs)}题)')

def Q(id, type_, difficulty, stem, options=None, answer='', acceptable=None, solution=None, hints=None, common=None, kp=None, time=90, irt=None, tags=None):
    q = {'id': id, 'type': type_, 'difficulty': difficulty,
         'difficultyLabel': ['入门','基础','进阶','拔高','竞赛'][difficulty-1],
         'stem': stem}
    if options: q['options'] = options
    if answer: q['answer'] = answer
    if acceptable: q['acceptableAnswers'] = acceptable
    if solution: q['solution'] = solution
    if hints: q['hints'] = hints
    if common: q['commonMistakes'] = common
    if kp: q['knowledgePoints'] = kp
    if time: q['estimatedTime'] = time
    if irt: q['irtParams'] = irt
    if tags: q['tags'] = tags
    return q

def mkcm(mistake, reason, correction, layer='L2'):
    return [{'mistake': mistake, 'reason': reason, 'correction': correction, 'errorLayer': layer}]

def irt(d):
    b = {1: -1.2, 2: -0.5, 3: 0.3, 4: 1.2, 5: 1.8}
    return {'a': 0.9 + (d-1) * 0.05, 'b': b.get(d, 0), 'c': 0.25}

