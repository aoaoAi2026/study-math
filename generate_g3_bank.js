const fs = require('fs');
const path = require('path');

const OUTDIR = path.join(__dirname, 'public', 'content', 'question-bank', 'g3');

function writeJson(file, data) {
  fs.writeFileSync(path.join(OUTDIR, file), JSON.stringify(data, null, 2), 'utf8');
  console.log('✓ ' + file + ' (' + data.questions.length + '题)');
}

const SOURCE = { type: 'textbook', name: '人教版三年级奥数', year: 2023 };

function diffLabel(d) {
  return ['', '入门', '基础', '进阶', '拔高', '竞赛'][d];
}

function qBase(opts) {
  return Object.assign({
    image: '',
    options: [],
    acceptableAnswers: [],
    hints: [],
    commonMistakes: [],
    source: SOURCE,
    knowledgePoints: [],
    estimatedTime: 60,
    irtParams: { a: 1.0, b: 0.0, c: 0.25 },
    tags: []
  }, opts);
}

function mkB(b, id, name, total, qs) {
  return { topicId: id, topicName: name, grade: 3, category: b, version: '1.0', totalQuestions: total, questions: qs };
}

// ======== 通用题型构造 ========
function choice(id, difficulty, stem, options, answer, solution, hints, mistakes, kps, tags) {
  return qBase({
    id, type: 'choice', difficulty, difficultyLabel: diffLabel(difficulty),
    stem, options, answer, acceptableAnswers: [answer],
    solution, hints, commonMistakes: mistakes,
    knowledgePoints: kps, tags,
    irtParams: { a: 1.0, b: [-2, -1, 0, 1, 2][difficulty - 1] || 0, c: 0.25 }
  });
}
function fill(id, difficulty, stem, answer, solution, hints, mistakes, kps, tags) {
  return qBase({
    id, type: 'fill', difficulty, difficultyLabel: diffLabel(difficulty),
    stem, answer, acceptableAnswers: [answer, String(answer).trim()],
    solution, hints, commonMistakes: mistakes,
    knowledgePoints: kps, tags,
    irtParams: { a: 1.0, b: [-2, -1, 0, 1, 2][difficulty - 1] || 0, c: 0.25 }
  });
}
function input(id, difficulty, stem, answer, solution, hints, mistakes, kps, tags) {
  return qBase({
    id, type: 'input', difficulty, difficultyLabel: diffLabel(difficulty),
    stem, answer, acceptableAnswers: [answer, String(answer).trim()],
    solution, hints, commonMistakes: mistakes,
    knowledgePoints: kps, tags,
    irtParams: { a: 1.0, b: [-2, -1, 0, 1, 2][difficulty - 1] || 0, c: 0.25 }
  });
}

