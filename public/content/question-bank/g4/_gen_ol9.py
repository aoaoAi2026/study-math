def mkq(id_, type_, diff, stem, opts, ans, acceptable, sol, hints, mlist, kps, et, tags):
    return {"id": id_, "type": type_, "difficulty": diff, "difficultyLabel": dl[diff][0],
            "stem": stem, "image": "", "options": opts, "answer": ans,
            "acceptableAnswers": acceptable, "solution": sol, "hints": hints,
            "commonMistakes": mlist, "source": {"type": "textbook", "name": "人教版四年级", "year": 2023},
            "knowledgePoints": kps, "estimatedTime": et,
            "irtParams": {"a": 1.0, "b": dl[diff][1], "c": 0.25}, "tags": tags}

def mk(mi, rsn, corr, layer="L2"):
    return [{"mistake": mi, "reason": rsn, "correction": corr, "errorLayer": layer}]

def save(tid, tname, qs):
    data = {"topicId": tid, "topicName": tname, "grade": 4, "category": "olympiad",
            "version": "1.0", "totalQuestions": len(qs), "questions": qs}
    with open(os.path.join(base_dir, tid + ".json"), "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"  ✓ {tid}: {len(qs)}题")

