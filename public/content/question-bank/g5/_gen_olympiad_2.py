def make_q(id_, typ, diff, stem, opts, ans, accept, sol, hints, m_list, kp, t, tags):
    lb, b = DIFF[diff]
    ms = []
    for m in m_list:
        if len(m) >= 4:
            ms.append({"mistake":m[0],"reason":m[1],"correction":m[2],"errorLayer":m[3]})
        elif len(m) == 3:
            ms.append({"mistake":m[0],"reason":m[1],"correction":m[1],"errorLayer":m[2]})
    return {"id":id_,"type":typ,"difficulty":diff,"difficultyLabel":lb,"stem":stem,
            "image":"","options":opts,"answer":ans,"acceptableAnswers":accept,
            "solution":sol,"hints":hints,"commonMistakes":ms,
            "source":{"type":"olympiad","name":"奥数训练","year":2024},
            "knowledgePoints":kp,"estimatedTime":t,"irtParams":{"a":1.0,"b":b,"c":0.25},"tags":tags}

def save(tid, tname, cat, qs):
    data = {"topicId":tid,"topicName":tname,"grade":5,"category":cat,"version":"1.0","totalQuestions":len(qs),"questions":qs}
    with open(os.path.join(base,tid+".json"),"w",encoding="utf-8") as f:
        json.dump(data,f,ensure_ascii=False,indent=2)
    print("  ✓ "+tname)

