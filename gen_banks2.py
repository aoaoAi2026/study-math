def make(topicId, topicName, category, questions):
    data = {
        "topicId": topicId,
        "topicName": topicName,
        "grade": 3,
        "category": category,
        "version": "1.0",
        "totalQuestions": len(questions),
        "questions": questions
    }
    with open(os.path.join(OUT, topicId + ".json"), "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"OK {topicId}.json ({len(questions)}题)")

def q(id, typ, difficulty, stem, options, answer, solution, hints, mistakes, kps, tags, est=60, b=None):
    diff_map = {"1":"入门","2":"基础","3":"进阶","4":"拔高","5":"竞赛"}
    return {
        "id": id, "type": typ, "difficulty": int(difficulty),
        "difficultyLabel": diff_map[str(difficulty)],
        "stem": stem, "image": "",
        "options": list(options) if options else [],
        "answer": str(answer),
        "acceptableAnswers": [str(answer)],
        "solution": list(solution),
        "hints": list(hints),
        "commonMistakes": list(mistakes),
        "source": {"type": "textbook", "name": "人教版三年级奥数", "year": 2023},
        "knowledgePoints": list(kps),
        "estimatedTime": est,
        "irtParams": {"a": 1.0, "b": float(b) if b is not None else (int(difficulty)-3)*0.7, "c": 0.25},
        "tags": list(tags)
    }

